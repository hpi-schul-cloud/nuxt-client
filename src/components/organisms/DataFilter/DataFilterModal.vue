<template>
	<base-modal :active="true" @onBackdropClick="$emit('cancel')">
		<template #header> Filter: {{ $filters.entities(filterTitle) }} </template>
		<template #body>
			<slot />
		</template>
		<template #footer>
			<modal-footer>
				<template #left>
					<v-btn variant="text" class="btn-left" @click="$emit('remove')">
						{{ labelRemove }}
					</v-btn>
				</template>
				<template #right>
					<v-btn variant="text" @click="$emit('cancel')">
						{{ labelCancel }}
					</v-btn>
					<v-btn color="primary" variant="flat" @click="$emit('apply')">
						{{ labelApply }}
					</v-btn>
				</template>
			</modal-footer>
		</template>
	</base-modal>
</template>

<script>
import ModalFooter from "@/components/molecules/ModalFooter";
import { decode } from "html-entities";

export default {
	components: {
		ModalFooter,
	},
	filters: {
		entities(value) {
			return decode(value);
		},
	},
	props: {
		title: { type: String, required: true },
		labelApply: { type: String, required: true },
		labelCancel: { type: String, required: true },
		labelRemove: { type: String, required: true },
	},
};
</script>
