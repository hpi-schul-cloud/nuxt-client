<template>
	<section>
		<h2 class="text-h4 mb-10">
			{{ $t("pages.administration.school.index.authSystems.title") }}
		</h2>
		<v-text-field
			v-if="customLoginLinkEnabled && !hasSystems"
			id="school-login-link-0"
			:value="generateLoginLink()"
			class="school-login-link"
			:color="getCopyStatus(0) ? 'success' : 'primary'"
			:label="
				$t('pages.administration.school.index.authSystems.loginLinkLabel')
			"
			readonly
			dense
			@blur="linkCopyFinished(0)"
		>
			<template #append>
				<v-btn
					icon
					@click="copyLoginLink(0)"
					:aria-label="
						$t('pages.administration.school.index.authSystems.copyLink')
					"
				>
					<v-icon>
						{{ getCopyStatus(0) ? iconMdiCheckCircle : iconMdiContentCopy }}
					</v-icon>
				</v-btn>
			</template>
		</v-text-field>
		<v-simple-table v-if="hasSystems" class="table-system">
			<template #default>
				<thead>
					<tr>
						<th class="text-left">
							{{ $t("pages.administration.school.index.authSystems.alias") }}
						</th>
						<th class="text-left">
							{{ $t("pages.administration.school.index.authSystems.type") }}
						</th>
						<th v-if="customLoginLinkEnabled" class="text-left">
							{{
								$t(
									"pages.administration.school.index.authSystems.loginLinkLabel"
								)
							}}
						</th>
						<th class="text-left"></th>
					</tr>
				</thead>
				<tbody>
					<tr v-for="system in systems" :key="system._id">
						<td>{{ system.alias }}</td>
						<td>{{ system.type }}</td>
						<td v-if="customLoginLinkEnabled">
							<v-text-field
								v-if="isLoginSystem(system)"
								:id="`school-login-link-${system._id}`"
								:value="generateLoginLink(system)"
								class="school-login-link"
								:color="getCopyStatus(system._id) ? 'success' : 'primary'"
								readonly
								dense
								@blur="linkCopyFinished"
							>
								<template #append>
									<v-btn
										icon
										@click="copyLoginLink(system._id)"
										:aria-label="
											$t(
												'pages.administration.school.index.authSystems.copyLink'
											)
										"
									>
										<v-icon>{{
											getCopyStatus(system._id)
												? iconMdiCheckCircle
												: iconMdiContentCopy
										}}</v-icon>
									</v-btn>
								</template>
							</v-text-field>
						</td>
						<td>
							<v-btn
								v-if="isEditable(system) && hasSystemEditPermission"
								class="edit-system-btn"
								icon
								:to="`/administration/ldap/config?id=${system._id}`"
								:aria-label="ariaLabels(system).edit"
							>
								<v-icon>{{ iconMdiPencilOutline }}</v-icon>
							</v-btn>
							<v-btn
								v-if="isRemovable(system) && hasSystemCreatePermission"
								class="delete-system-btn"
								icon
								:aria-label="ariaLabels(system).delete"
								@click.stop="openConfirmDeleteDialog(system._id)"
							>
								<v-icon>{{ iconMdiTrashCanOutline }}</v-icon>
							</v-btn>
						</td>
					</tr>
				</tbody>
			</template>
		</v-simple-table>
		<v-btn
			v-if="hasSystemCreatePermission"
			color="primary"
			class="my-8 add-ldap"
			variant="flat"
			to="/administration/ldap/config"
		>
			{{ $t("pages.administration.school.index.authSystems.addLdap") }}
		</v-btn>
		<v-custom-dialog
			v-model="confirmDeleteDialog.isOpen"
			class="custom-dialog"
			:size="375"
			has-buttons
			@dialog-confirmed="removeSystem(confirmDeleteDialog.systemId)"
		>
			<h2 slot="title" class="text-h4 my-2">
				{{
					$t("pages.administration.school.index.authSystems.deleteAuthSystem")
				}}
			</h2>
			<template slot="content">
				<p class="text-md mt-2">
					{{
						$t(
							"pages.administration.school.index.authSystems.confirmDeleteText"
						)
					}}
				</p>
			</template>
		</v-custom-dialog>
	</section>
</template>

<script>
import { authModule, envConfigModule, schoolsModule } from "@/store";
import {
	mdiPencilOutline,
	mdiTrashCanOutline,
	mdiContentCopy,
	mdiCheckCircle,
} from "@mdi/js";
import vCustomDialog from "@/components/organisms/vCustomDialog";

export default {
	components: {
		vCustomDialog,
	},
	props: {
		systems: {
			type: Array,
			required: true,
		},
	},
	data() {
		return {
			confirmDeleteDialog: {
				isOpen: false,
				systemId: undefined,
			},
			iconMdiPencilOutline: mdiPencilOutline,
			iconMdiTrashCanOutline: mdiTrashCanOutline,
			iconMdiContentCopy: mdiContentCopy,
			iconMdiCheckCircle: mdiCheckCircle,
			copiedElement: "",
		};
	},
	computed: {
		hasSystems() {
			return this.systems.length > 0;
		},
		customLoginLinkEnabled: () => envConfigModule.getLoginLinkEnabled,
		hasSystemCreatePermission: () => {
			return authModule.getUserPermissions.includes("system_create");
		},
		hasSystemEditPermission: () => {
			return authModule.getUserPermissions.includes("system_edit");
		},
	},
	methods: {
		ariaLabels(system) {
			const systemName =
				system.alias ||
				this.$t("pages.administration.school.index.authSystems.title");

			return {
				edit: this.$t("pages.administration.school.index.authSystems.edit", {
					system: systemName,
				}),
				delete: this.$t(
					"pages.administration.school.index.authSystems.delete",
					{ system: systemName }
				),
			};
		},
		isEditable(system) {
			return system.type === "ldap" && system.ldapConfig.provider === "general";
		},
		isRemovable(system) {
			return system.type !== "ldap" || system.ldapConfig.provider === "general";
		},
		openConfirmDeleteDialog(systemId) {
			this.confirmDeleteDialog = {
				isOpen: true,
				systemId,
			};
		},
		isLoginSystem(system) {
			return system.oauthConfig || system.ldapConfig;
		},
		removeSystem(systemId) {
			schoolsModule.deleteSystem(systemId);
			// TODO show error
		},
		generateLoginLink(system) {
			let params = "";

			if (!system) {
				params = "?strategy=email";
			} else if (system.oauthConfig) {
				params = `?strategy=${system.oauthConfig.provider}`;
			} else if (system.ldapConfig) {
				params = `?strategy=ldap&schoolId=${schoolsModule.getSchool.id}`;
			}

			return `${window.location.origin}/login${params}`;
		},
		copyLoginLink(id) {
			const copyText = document.getElementById(`school-login-link-${id}`);

			copyText.select();
			copyText.setSelectionRange(0, 99999); // For mobile devices

			navigator.clipboard.writeText(copyText.value);

			this.copiedElement = id;
		},
		getCopyStatus(id) {
			return this.copiedElement === id;
		},
		linkCopyFinished() {
			this.copiedElement = "";
		},
	},
};
</script>
